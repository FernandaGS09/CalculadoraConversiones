    private void cantidadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cantidadKeyReleased
        // TODO add your handling code here:
        
        float cant, res1, res2;
        
        cant=Float.parseFloat(cantidad.getText());
        
        String unidad = (String) menu.getSelectedItem();
        
        if(unidad.equals("K")){
            res1=(float) (cant-273.15);
            r1.setText("" + res1);
            
            res2=(float) ((9*(cant-273.15)/5)+32);
            r2.setText("" + res2);
        }
        
        if(unidad.equals("C")){
            res1=(float) (cant+273.15);
            r1.setText("" + res1);
            
            res2=(float) (((9*cant)/5)+32);
            r2.setText("" + res2);
        }
        
        if(unidad.equals("F")){
         
            res1=(float) (((5*(cant-32))/9)+273.15);
            r1.setText("" + res1);
            
            res2=(float) ((5*(cant-32))/9);
            r2.setText("" + res2);
        }
    }//GEN-LAST:event_cantidadKeyReleased

