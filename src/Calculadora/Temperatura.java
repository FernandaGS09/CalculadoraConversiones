    private void menuItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_menuItemStateChanged
        // TODO add your handling code here:
       
        String unidad = (String) menu.getSelectedItem();
        
        if(unidad.equals("K")){
            nombre.setText("Kelvin");
            u1.setText("Celcius");
            a1.setText("C");
            u2.setText("Fahrenheit");
            a2.setText("F");
        }
        
        if(unidad.equals("C")){
            nombre.setText("Celcius");
            u1.setText("Kelvin");
            a1.setText("K");
            u2.setText("Fahrenheit");
            a2.setText("F");
        }
        
        if(unidad.equals("F")){
            nombre.setText("Fahrenheit");
            u1.setText("Kelvin");
            a1.setText("K");
            u2.setText("Celcius");
            a2.setText("C");
        }
        
    }//GEN-LAST:event_menuItemStateChanged

